const IMG_CACHE = new Map();
let currentIndex = -1;
let viewing = false;
let keysBound = false;
let lastActiveEl = null;

function setFetchPriority(img, priority) {
	try {
		img.fetchPriority = priority;
		img.setAttribute("fetchpriority", priority);
	} catch {}
}

function loadImage(url, priority = "auto") {
	if (!url) return Promise.reject(new Error("No URL"));
	
	const cached = IMG_CACHE.get(url);
	if (cached?.state === "loaded") return Promise.resolve(cached.img);
	if (cached?.promise) return cached.promise;

	const img = new Image();
	img.decoding = "async";
	img.crossOrigin = "anonymous";
	setFetchPriority(img, priority);

	const promise = new Promise((resolve, reject) => {
		let settled = false;
		const done = () => {
			if (settled) return;
			settled = true;
			IMG_CACHE.set(url, { img, state: "loaded", promise: null });
			resolve(img);
		};
		const fail = (err) => {
			if (settled) return;
			settled = true;
			IMG_CACHE.set(url, { img, state: "error", promise: null });
			reject(err || new Error("Image load failed"));
		};
		img.onload = done;
		img.onerror = fail;
		img.src = url;
	});

	IMG_CACHE.set(url, { img, state: "loading", promise });
	return promise;
}

function hintPreload(url, priority = "low") {
	if (!url) return;
	loadImage(url, priority).catch(() => {});
}

function normalizeSrc(photo) {
	if (!photo) return { thumb: null, preview: null, full: null };
	if (typeof photo.src === "string") {
		return { thumb: photo.src, preview: photo.src, full: photo.src };
	}
	const obj = photo.src || {};
	const full = obj.full || obj.preview || obj.medium || obj.thumb || null;
	const preview = obj.preview || obj.medium || obj.thumb || full || null;
	const thumb = obj.thumb || obj.preview || obj.medium || full || null;
	return { thumb, preview, full };
}

function nextFrame() {
	return new Promise(r => requestAnimationFrame(() => requestAnimationFrame(r)));
}

function ric(cb) {
	if ("requestIdleCallback" in window) {
		return requestIdleCallback(cb, { timeout: 1200 });
	}
	return setTimeout(cb, 0);
}

function createCard(photo, index) {
	const card = document.createElement("article");
	card.className = "card";
	card.dataset.cursor = "grow";
	card.dataset.index = String(index);
	card.tabIndex = 0;
	card.setAttribute("role", "button");
	card.setAttribute("aria-label", photo.alt || "Open photo");

	const thumb = document.createElement("div");
	thumb.className = "thumb";

	const img = document.createElement("img");
	img.loading = "lazy";
	img.decoding = "async";
	img.draggable = false;
	img.alt = photo.alt || "";
	img.style.width = "100%";
	img.style.height = "100%";
	img.style.objectFit = "cover";
	img.style.display = "block";

	const srcs = normalizeSrc(photo);
	img.src = srcs.thumb || srcs.preview || srcs.full || "";

	thumb.appendChild(img);
	card.appendChild(thumb);

	if (photo.title || photo.metadata) {
		const body = document.createElement("div");
		body.className = "card-body";

		const title = document.createElement("h3");
		title.textContent = photo.title || `Photo ${index + 1}`;
		title.className = "card-title";
		body.appendChild(title);

		if (photo.metadata?.technical && photo.metadata?.settings) {
			const meta = document.createElement("p");
			meta.className = "meta quiet";
			meta.textContent = `${photo.metadata.technical.megapixels} • ${photo.metadata.settings.aperture} • ${photo.metadata.settings.shutter}`;
			body.appendChild(meta);
		}

		card.appendChild(body);
	}

	const open = () => openViewer(index);
	card.addEventListener("click", open);
	card.addEventListener("keydown", e => {
		if (e.key === "Enter" || e.key === " ") {
			e.preventDefault();
			open();
		}
	});

	const warm = () => {
		const s = normalizeSrc(photo);
		hintPreload(s.preview, "low");
	};
	card.addEventListener("mouseenter", warm, { passive: true });
	card.addEventListener("focusin", warm, { passive: true });
	card.addEventListener("pointerdown", warm, { passive: true });
	card.addEventListener("touchstart", warm, { passive: true });

	return card;
}

function getViewerParts() {
	return {
		root: document.getElementById("photo-viewer"),
		backdrop: document.getElementById("photo-viewer-backdrop"),
		frame: document.getElementById("photo-viewer-frame"),
		title: document.getElementById("photo-viewer-title"),
		filename: document.getElementById("photo-viewer-filename"),
		img: document.getElementById("photo-viewer-img"),
		metadata: document.getElementById("photo-viewer-metadata"),
		downloadBtn: document.getElementById("photo-viewer-download"),
		closeBtn: document.getElementById("photo-viewer-close")
	};
}

function focusables(root) {
	return Array.from(
		root.querySelectorAll('button, [href], input, select, textarea, [tabindex]:not([tabindex="-1"])')
	).filter(el => !el.hasAttribute("disabled") && !el.getAttribute("aria-hidden") && el.offsetParent !== null);
}

function bindViewerKeys() {
	if (keysBound) return;
	keysBound = true;

	document.addEventListener("keydown", e => {
		if (!viewing) return;

		if (e.key === "Escape") {
			e.preventDefault();
			closeViewer();
			return;
		}
		if (e.key === "ArrowRight") {
			e.preventDefault();
			step(1);
			return;
		}
		if (e.key === "ArrowLeft") {
			e.preventDefault();
			step(-1);
			return;
		}
		if (e.key === "Tab") {
			const { root } = getViewerParts();
			if (!root) return;
			const list = focusables(root);
			if (!list.length) return;
			const first = list[0];
			const last = list[list.length - 1];
			if (e.shiftKey) {
				if (document.activeElement === first) {
					last.focus();
					e.preventDefault();
				}
			} else if (document.activeElement === last) {
				first.focus();
				e.preventDefault();
			}
		}
	});
}

async function openViewer(index) {
	const p = PHOTOS[index];
	const { root, title, filename, img, metadata, downloadBtn, closeBtn } = getViewerParts();
	if (!p || !root || !img) return;

	currentIndex = index;
	viewing = true;
	lastActiveEl = document.activeElement;

	const srcs = normalizeSrc(p);
	const initialSrc = srcs.thumb || srcs.preview || srcs.full || "";

	title.textContent = p.title || `Photo ${index + 1}`;
	filename.textContent = p.filename || `photo-${index + 1}.jpg`;
	img.alt = p.alt || "";
	img.draggable = false;
	img.loading = "eager";
	img.src = initialSrc;

	root.hidden = false;
	document.documentElement.classList.add("no-scroll");
	closeBtn?.focus?.();

	await nextFrame();

	if (srcs.preview && initialSrc !== srcs.preview) {
		loadImage(srcs.preview, "low")
			.then(() => {
				if (currentIndex !== index) return;
				requestAnimationFrame(() => {
					if (img.src !== srcs.preview) img.src = srcs.preview;
				});
			})
			.catch(() => {});
	}

	if (downloadBtn) downloadBtn.onclick = () => downloadImage(p);

	if (metadata && p.metadata) {
		ric(() => {
			if (currentIndex === index) {
				metadata.innerHTML = buildMetadataHTML(p.metadata, p.copyright);
			}
		});
	}

	bindViewerKeys();
	ric(() => prefetchNeighbors(index));
}

